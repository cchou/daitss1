package edu.fcla.daitss.util;

import edu.fcla.daitss.exception.PackageException;

/**
 * VirusFoundException is used when a virus is found within a file.
 * It causes the file's package to be rejected during ingest.
 * 
 * @author Andrea Goethals, FCLA
 *
 */
public class VirusFoundException extends PackageException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1517218539462880031L;

	/**
	 * 
	 */
	public VirusFoundException() {
		super();
	}

	/**
	 * @param message
	 */
	public VirusFoundException(String message) {
		super(message);
	}

	/**
	 * @param message
	 * @param cause
	 */
	public VirusFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	/**
	 * @param cause
	 */
	public VirusFoundException(Throwable cause) {
		super(cause);
	}
}
